-- Quick fix: Delete documents with invalid IDs
-- WARNING: This will permanently delete the documents

-- Show what will be deleted
SELECT id, title, content FROM document 
WHERE id !~ '^[0-9a-f]{8}-[0-9a-f]{4}-[1-5][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$';

-- Delete suggestions first (foreign key constraint)
DELETE FROM suggestion 
WHERE document_id IN (
  SELECT id FROM document 
  WHERE id !~ '^[0-9a-f]{8}-[0-9a-f]{4}-[1-5][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$'
);

-- Delete invalid documents
DELETE FROM document 
WHERE id !~ '^[0-9a-f]{8}-[0-9a-f]{4}-[1-5][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$';

-- Verify cleanup
SELECT COUNT(*) as remaining_invalid_docs FROM document 
WHERE id !~ '^[0-9a-f]{8}-[0-9a-f]{4}-[1-5][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$';
